-- PUBLISHER TABLE

CREATE TABLE PUBLISHER
(PUBNAME VARCHAR(20) PRIMARY KEY,
ADDRESS VARCHAR(20),
PHONE INT);

-- BOOK TABLE

CREATE TABLE BOOK
(BID INT PRIMARY KEY,
TITLE VARCHAR(20),
PUBNAME VARCHAR(20) REFERENCES PUBLISHER (PUBNAME) ON DELETE CASCADE,
PUBYEAR VARCHAR(20));


-- BOOK AUTHOR TABLE

CREATE TABLE BOOK_AUTHORS
(BID INTEGER REFERENCES BOOK (BID),
ANAME VARCHAR(20),
PRIMARY KEY (BID, ANAME));

-- LIBRARY PROGRAMME

CREATE TABLE LIBRARY_PROGRAMME
(PID INT PRIMARY KEY,
PNAME VARCHAR(50),
ADDRESS VARCHAR(50));

-- BOOK COPIES TABLE

CREATE TABLE BOOK_COPIES
(BID INTEGER REFERENCES BOOK (BID) ON DELETE CASCADE,
PID INTEGER REFERENCES LIBRARY_PROGRAMME (PID) ON DELETE CASCADE,
NOOFCOPIES INTEGER,
PRIMARY KEY (BID, PID));

-- BOOKLENDING TABLE

CREATE TABLE BOOKLENDING
(
BID INTEGER REFERENCES BOOK(BID) ON DELETE CASCADE,
PID INTEGER REFERENCES LIBRARY_PROGRAMME(PID) ON DELETE CASCADE,
CARDNO INTEGER,
DATEOUT DATE,
DUEDATE DATE,
PRIMARY KEY (BID,PID));


